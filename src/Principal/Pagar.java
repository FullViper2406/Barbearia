    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // carregar dados na tabela
         try {
            PrecoDAO objfuncionariodao = new PrecoDAO();
            DefaultTableModel model = (DefaultTableModel) jDadosf.getModel();
            model.setNumRows(0);
            ArrayList<PrecoDTO> Aluno = objfuncionariodao.PesquisarPreco();
            for (int num = 0; num < Aluno.size(); num++) {
                model.addRow(new String[]{
                    Aluno.get(num).getCabelo(),
                    Aluno.get(num).getBarba(),
                    Aluno.get(num).getSobrancelha(),
                });

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores VIEW: " + erro);
        }         // TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed

