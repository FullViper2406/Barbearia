    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Cadastrar pagamento
        Nome = jNome.getText();

        Data = jData.getText();

        Valor= jValor.getText();

        PagarDTO objalunodto = new PagarDTO();
        objalunodto.setNome(Nome);

        objalunodto.setData(Data);

        objalunodto.setValor(Valor);
        //mensagem pra campos vazios
        if(jNome.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Campo  obrigat칩rio,o preencha por favor !!","Aviso",JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(jData.getText().equals("  /  /    ")){
            JOptionPane.showMessageDialog(null,"Campo  obrigat칩rio,o preencha por favor !!","Aviso",JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(jValor.getText().equals("  ,  ")){
            JOptionPane.showMessageDialog(null,"Campo  obrigat칩rio,o preencha por favor !!","Aviso",JOptionPane.WARNING_MESSAGE);
            return;
        }

        PagarDAO objalunodao = new PagarDAO();
        objalunodao.cadastar(objalunodto);
    }//GEN-LAST:event_jButton3ActionPerformed

